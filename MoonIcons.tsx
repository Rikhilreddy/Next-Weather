export function getMoonIcon(phase: number) {
  let MoonIcon, phase_str;

  MoonIcon = NewMoonIcon;
  phase_str = "New Moon";

  if (phase === 0 || phase === 1) {
    MoonIcon = NewMoonIcon;
    phase_str = "New Moon";
  } else if (phase > 0 && phase < 0.25) {
    MoonIcon = MoonWaxingCrescentIcon;
    phase_str = "Waxing Crescent";
  } else if (phase === 0.25) {
    MoonIcon = MoonFirstQuandrantIcon;
    phase_str = "First Quandrant";
  } else if (phase > 0.25 && phase < 0.5) {
    MoonIcon = MoonWaxingGibousIcon;
    phase_str = "Waxing Gibous";
  } else if (phase === 0.5) {
    MoonIcon = FullMoonIcon;
    phase_str = "Full Moon";
  } else if (phase > 0.5 && phase < 0.75) {
    MoonIcon = MoonWaningGibousIcon;
    phase_str = "Waning Gibous";
  } else if (phase === 0.75) {
    MoonIcon = MoonLastQuadrantIcon;
    phase_str = "Third Quadrant";
  } else if (phase > 0.75 && phase < 1) {
    MoonIcon = MoonWaningCrescentIcon;
    phase_str = "Waning Crescent";
  }

  return {
    MoonIcon,
    phase_str,
  };
}

export function MoonPhase({ phase }: { phase: number }) {
  const { MoonIcon, phase_str } = getMoonIcon(phase);

  return (
    <div className="flex flex-col gap-2 justify-center items-center ">
      <MoonIcon className="w-8 h-8 rounded-full shadow-md dark:bg-gray-300 dark:text-gray-700" />
      <p>{phase_str}</p>
    </div>
  );
}
